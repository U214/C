/*
	회문(Palindrome)은 앞 or 뒤에서 읽어도 동일한 단어를 뜻한다.
	noon, level 과 같은 단어들이 회문에 속한다.
	사용자로부터 문자열을 입력받은후 회문인지 아닌지 
	판단하는 프로그램을 작성하시오.
	대소문자까지 동일해야 회문으로 인정함.
	출력예시)
	단어를 입력하시오. : noon
	회문입니다.
	단어를 입력하시오. : love
	회문이 아닙니다.
*/

#pragma warning(disable : 4996)
#include <stdio.h>

int main19(void)
{
	int length = 0;
	char str[50];

	printf("단어를 입력하시오 : ");
	scanf("%s", str);

	while (str[length] != '\0') {
		length++;
	}

	for (int i = 0; i < length / 2; i++) {
		if (str[i] != str[length - i - 1]) {
			printf("회문이 아닙니다. \n");
			return 0;
		}
	}

	printf("회문입니다. \n");

	return 0;
}